package day2;

public class operators {
	public static void main(String[] args) {
		int i=10;
		
		
		i+=10;
		System.out.println(i);
		i=10;
		i-=10;
		System.out.println(i);
		i=10;
		i*=10;
		System.out.println(i);
		i=10;
		i/=10;
		System.out.println(i);
		
		i=10;
		i%=10;
		System.out.println(i);
		
	}

}
